#include "libft.h"

int	ft_atoi_hexa(char s)
{
	int		c;

	c = 0;
	if ('a' <= s && 'f' >= s)
		c = s - 87;
	else if ('A' <= s && 'F' >= s)
		c = s - 55;
	else if (ft_isdigit(s))
		c = (c * 10) + (s - '0');
	return (c);
}
